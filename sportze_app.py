    RIGHT_HIP = 24
    RIGHT_KNEE = 26
    RIGHT_ANKLE = 28

    knee_angles = []

    for frame in landmark_frames:
        if not frame.get("has_landmarks"):
            continue
        lms = frame["landmarks"]
        try:
            lh = (lms[LEFT_HIP]["x"], lms[LEFT_HIP]["y"])
            lk = (lms[LEFT_KNEE]["x"], lms[LEFT_KNEE]["y"])
            la = (lms[LEFT_ANKLE]["x"], lms[LEFT_ANKLE]["y"])
            rh = (lms[RIGHT_HIP]["x"], lms[RIGHT_HIP]["y"])
            rk = (lms[RIGHT_KNEE]["x"], lms[RIGHT_KNEE]["y"])
            ra = (lms[RIGHT_ANKLE]["x"], lms[RIGHT_ANKLE]["y"])
        except Exception:
            continue

        left_angle = angle_3pt(lh, lk, la)
        right_angle = angle_3pt(rh, rk, ra)
        knee_angles.append((left_angle + right_angle) / 2)

    if not knee_angles:
        return {"summary": "No reliable squat landmarks found.", "feedback": []}

    min_knee_angle = min(knee_angles)
    depth_feedback = "Depth appears limited." if min_knee_angle > 95 else "Depth appears acceptable for a general squat."
    return {
        "summary": f"Estimated deepest average knee angle: {min_knee_angle:.1f}°",
        "feedback": [
            depth_feedback,
            "Check camera angle consistency: true side view improves squat assessment.",
            "For a more advanced review, add rep segmentation and torso-angle tracking.",
        ],
    }


@dataclass
class TrainingSession:
    sport: str
    objective: str
    duration_text: str
    summary: str
    blocks: List[SessionBlock] = field(default_factory=list)
    coaching_notes: List[str] = field(default_factory=list)
    progression_notes: List[str] = field(default_factory=list)
    red_flags: List[str] = field(default_factory=list)

    def to_markdown(self) -> str:
        output = []
        output.append(f"### {self.sport} session")
        output.append(f"**Objective:** {self.objective}")
        output.append(f"**Planned duration:** {self.duration_text}")
        output.append("")
        output.append(self.summary)
        output.append("")
        output.append("#### Session structure")
        for idx, block in enumerate(self.blocks, start=1):
            output.append(f"**{idx}. {block.title}**")
            output.append(block.detail)
            output.append("")
        if self.coaching_notes:
            output.append("#### Coaching notes")
            output.extend([f"- {n}" for n in self.coaching_notes])
            output.append("")
        if self.progression_notes:
            output.append("#### Progression")
            output.extend([f"- {n}" for n in self.progression_notes])
            output.append("")
        if self.red_flags:
            output.append("#### Safety / caution")
            output.extend([f"- {n}" for n in self.red_flags])
            output.append("")
        return "\n".join(output)


def clamp(value: float, min_value: float, max_value: float) -> float:
    return max(min_value, min(max_value, value))

def pick_by_rotation(options: List[Any], seed_key: str) -> Any:
    if not options:
        return None
    idx = abs(hash(seed_key)) % len(options)
    return options[idx]

def safe_int(value: Any, default: int = 0) -> int:
    try:
        return int(value)
    except Exception:
        return default

def safe_float(value: Any, default: float = 0.0) -> float:
    try:
        return float(value)
    except Exception:
        return default

def min_per_km_to_seconds(pace: str) -> int:
    if not pace or ":" not in pace:
        return 0
    parts = pace.strip().split(":")
    if len(parts) != 2:
        return 0
    minutes = safe_int(parts[0], 0)
    seconds = safe_int(parts[1], 0)
    return minutes * 60 + seconds

def seconds_to_pace(seconds: int) -> str:
    minutes = seconds // 60
    secs = seconds % 60
    return f"{minutes}:{secs:02d}/km"

def format_minutes_to_clock(total_minutes: int) -> str:
    hours = total_minutes // 60
    minutes = total_minutes % 60
    if hours <= 0:
        return f"{minutes} min"
    return f"{hours}h {minutes:02d}min"

def format_decimal_hours(hours: float) -> str:
    total_minutes = int(round(hours * 60))
    return format_minutes_to_clock(total_minutes)

def kilometers_to_pretty(km: float) -> str:
    if abs(km - int(km)) < 1e-9:
        return f"{int(km)} km"
    return f"{km:.1f} km"

def normalize_text(s: str) -> str:
    return (s or "").strip().lower()

def level_band(experience_years: float) -> str:
    if experience_years < 0.5:
        return "beginner"
    if experience_years < 2:
        return "novice"
    if experience_years < 5:
        return "intermediate"
    return "advanced"

def bool_label(value: bool) -> str:
    return "Yes" if value else "No"

def estimate_run_paces_from_goal(goal_event: str, level: str) -> Dict[str, str]:
    presets = {
        "5k": {"beginner": "6:45", "novice": "6:05", "intermediate": "5:15", "advanced": "4:20"},
        "10k": {"beginner": "6:55", "novice": "6:10", "intermediate": "5:20", "advanced": "4:25"},
        "half marathon": {"beginner": "7:05", "novice": "6:20", "intermediate": "5:30", "advanced": "4:35"},
        "marathon": {"beginner": "7:20", "novice": "6:35", "intermediate": "5:40", "advanced": "4:45"},
        "general endurance": {"beginner": "6:55", "novice": "6:15", "intermediate": "5:30", "advanced": "4:40"},
    }
    normalized = normalize_text(goal_event)
    group = presets.get(normalized, presets["general endurance"])
    return {
        "easy": group[level],
        "steady": seconds_to_pace(int(min_per_km_to_seconds(group[level]) * 0.95)),
        "tempo": seconds_to_pace(int(min_per_km_to_seconds(group[level]) * 0.90)),
        "interval": seconds_to_pace(int(min_per_km_to_seconds(group[level]) * 0.82)),
    }

def estimate_swim_paces(level: str) -> Dict[str, str]:
    presets = {
        "beginner": {"easy_100": "2:35", "steady_100": "2:25", "threshold_100": "2:12"},
        "novice": {"easy_100": "2:20", "steady_100": "2:10", "threshold_100": "2:00"},
        "intermediate": {"easy_100": "2:00", "steady_100": "1:52", "threshold_100": "1:42"},
        "advanced": {"easy_100": "1:42", "steady_100": "1:35", "threshold_100": "1:26"},
    }
    return presets[level]

def parse_hhmm_or_hours(value: str) -> Optional[int]:
    if not value:
        return None
    s = normalize_text(value).replace("hours", "").replace("hour", "").replace("hrs", "").replace("hr", "")
    s = s.replace("h", ":").replace(" ", "")
    if ":" in s:
        parts = s.split(":")
        if len(parts) >= 2:
            hours = safe_int(parts[0], 0)
            minutes = safe_int(parts[1], 0)
            return hours * 60 + minutes
    if "." in s:
        return int(round(safe_float(s, 0.0) * 60))
    numeric = safe_float(s, -1)
    if numeric < 0:
        return None
    if numeric <= 12:
        return int(round(numeric * 60))
    return int(round(numeric))

def fatigue_multiplier(readiness: int) -> float:
    readiness = clamp(readiness, 1, 10)
    return 0.75 + (readiness - 1) * (0.35 / 9)

def pretty_bullets(items: List[str]) -> str:
    return "\n".join(f"- {i}" for i in items)


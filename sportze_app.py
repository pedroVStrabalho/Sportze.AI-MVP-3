def mediapipe_pose_landmarks_available() -> bool:
    if mp is None:
        return False
    return hasattr(mp, "solutions") and hasattr(mp.solutions, "pose")

def sample_pose_frames(video_path: str, every_n_frames: int = 5, max_frames: int = 120) -> Dict[str, Any]:
    if cv2 is None or mp is None:
        return {"error": "Install both opencv-python and mediapipe first."}
    if not mediapipe_pose_landmarks_available():
        return {"error": "MediaPipe pose solution not available in this installation."}

    cap = cv2.VideoCapture(video_path)
    if not cap.isOpened():
        return {"error": "Could not open video."}

    pose = mp.solutions.pose.Pose(
        static_image_mode=False,
        model_complexity=1,
        smooth_landmarks=True,
        enable_segmentation=False,
        min_detection_confidence=0.5,
        min_tracking_confidence=0.5,
    )

    sampled = 0
    processed = 0
    landmarks_per_frame = []

    frame_idx = 0
    while cap.isOpened():
        ok, frame = cap.read()
        if not ok:
            break
        if frame_idx % every_n_frames != 0:
            frame_idx += 1
            continue

        rgb = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)
        result = pose.process(rgb)

        record = {"frame_idx": frame_idx, "has_landmarks": bool(result.pose_landmarks)}
        if result.pose_landmarks:
            record["landmarks"] = [
                {"x": lm.x, "y": lm.y, "z": lm.z, "visibility": lm.visibility}
                for lm in result.pose_landmarks.landmark
            ]
            sampled += 1
        landmarks_per_frame.append(record)
        processed += 1
        frame_idx += 1
        if processed >= max_frames:
            break

    cap.release()
    pose.close()

    return {
        "processed_samples": processed,
        "frames_with_landmarks": sampled,
        "landmarks_per_frame": landmarks_per_frame,
    }

def angle_3pt(a: Tuple[float, float], b: Tuple[float, float], c: Tuple[float, float]) -> float:
    ba = (a[0] - b[0], a[1] - b[1])
    bc = (c[0] - b[0], c[1] - b[1])
    dot = ba[0] * bc[0] + ba[1] * bc[1]
    norm_ba = math.sqrt(ba[0] ** 2 + ba[1] ** 2)
    norm_bc = math.sqrt(bc[0] ** 2 + bc[1] ** 2)
    if norm_ba == 0 or norm_bc == 0:
        return 0.0
    cosine = clamp(dot / (norm_ba * norm_bc), -1.0, 1.0)
    return math.degrees(math.acos(cosine))


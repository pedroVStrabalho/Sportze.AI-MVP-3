def target_long_run_distance(event_goal: str, weekly_km: float, current_long_run_km: float, readiness: int) -> float:
    g = normalize_text(event_goal)
    multiplier = fatigue_multiplier(readiness)
    if g == "marathon":
        base = max(current_long_run_km, weekly_km * 0.32)
        target = min(base + 2.0, weekly_km * 0.40, 34.0)
    elif g == "half marathon":
        base = max(current_long_run_km, weekly_km * 0.28)
        target = min(base + 1.5, weekly_km * 0.38, 24.0)
    elif g == "10k":
        base = max(current_long_run_km, weekly_km * 0.25)
        target = min(base + 1.0, weekly_km * 0.35, 18.0)
    elif g == "5k":
        base = max(current_long_run_km, weekly_km * 0.22)
        target = min(base + 1.0, weekly_km * 0.32, 14.0)
    else:
        base = max(current_long_run_km, weekly_km * 0.25)
        target = min(base + 1.0, weekly_km * 0.35, 18.0)
    return round(target * multiplier, 1)

def target_key_run_distance(event_goal: str, kind: str, session_minutes: int, weekly_km: float) -> float:
    g = normalize_text(event_goal)
    if kind == "intervals":
        if g == "5k":
            return clamp(weekly_km * 0.18, 4.0, 8.0)
        if g == "10k":
            return clamp(weekly_km * 0.16, 5.0, 10.0)
        if g == "half marathon":
            return clamp(weekly_km * 0.15, 6.0, 11.0)
        if g == "marathon":
            return clamp(weekly_km * 0.14, 6.0, 12.0)
    if kind in {"tempo", "half marathon pace", "marathon pace"}:
        return clamp(session_minutes / 10, 5.0, 18.0)
    if kind == "steady progression":
        return clamp(session_minutes / 8.5, 6.0, 22.0)
    if kind == "easy aerobic":
        return clamp(session_minutes / 7.5, 5.0, 16.0)
    return clamp(session_minutes / 8.0, 4.0, 14.0)


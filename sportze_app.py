def safety_red_flags(injury_status: str, pain_score: int) -> List[str]:
    flags = []
    text = normalize_text(injury_status)
    if pain_score >= 7:
        flags.append("Pain is high today. Reduce load sharply and consider professional evaluation before intense work.")
    if "swelling" in text or "sharp" in text:
        flags.append("Sharp pain or swelling can indicate a more acute issue. Avoid hard training until assessed.")
    if "recent injury" in text or "return to play" in text:
        flags.append("Return-to-play work should stay conservative and technique-focused.")
    if "dizzy" in text or "faint" in text or "chest" in text:
        flags.append("Symptoms such as dizziness or chest pain are not training-normal and need medical attention.")
    return flags

def apply_readiness(base_minutes: int, readiness: int) -> int:
    return max(20, int(round(base_minutes * fatigue_multiplier(readiness))))

def choose_gym_split(days_per_week: int, preference: str) -> str:
    pref = normalize_text(preference)
    if pref in GYM_SPLITS:
        return pref
    if days_per_week <= 3:
        return "full body"
    if days_per_week == 4:
        return "upper lower"
    return "push pull legs"

